// VARIAVEIS
var nome = 'Alify Formidable';

// PROPRIEDADE length
document.write(nome.length); // Retorna o tamanho de caracteres na String
document.write('<br>');
document.write(nome.charAt(2)); // Retorna o valor armazenado na posição da String
document.write('<br>');
document.write(nome.indexOf('g')); //Retorna a posição da letra da String
document.write('<br>');
document.write(nome.replace('Sant Ana', 'Silva')); // Substituição de valores na String
document.write('<br>');
document.write(nome.substring(4,10)); // Retorna String com valores entre uma coluna de String denominada com começo e fim
document.write('<br>');
document.write(nome.toLowerCase()); // Deixa toda String LOW CASE - MINÚSCULAS
document.write('<br>');
document.write(nome.toUpperCase()); // Deixa toda String UPPER CASE - MAÍUSCULAS
document.write('<br>');
document.write(nome.trim()); // Remove os espaços em branco da String
document.write('<br>');
document.write(nome.valueOf());
document.write('<br>');
document.write('<br>');
document.write('<hr>');
document.wirte('<h2> Funções - Matemáticas </h2>');